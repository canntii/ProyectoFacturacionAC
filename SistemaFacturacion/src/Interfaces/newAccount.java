    private void textRegisterMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_textRegisterMousePressed
        // TODO add your handling code here:
        String nombre = this.typeName.getText();
        String apellido = this.typeApellido.getText();
        String telefono = this.typePhone.getText();
        String correoVendedor = this.typeEmail.getText();
        String passVendedor = String.valueOf(this.typePass.getPassword());
        String query = "INSERT INTO vendedores (nombreVendedor, apellidoVendedor,telefonoVendedor,correoVendedor,passw) VALUES (?,?,?,?,?) ";
        try {
            Connection connet = Conectar();
            PreparedStatement ps = connet.prepareStatement(query);
            ps.setString(1, nombre);
            ps.setString(2, apellido);
            ps.setString(3, telefono);
            ps.setString(4, correoVendedor);
            ps.setString(5, passVendedor);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro exitoso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_textRegisterMousePressed

